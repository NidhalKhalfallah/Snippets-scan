const { DSA } = require('crypto');

// Generate a DSA key
const dsa = DSA.generateKey(1024);

// Sign the message
const signature = dsa.sign('Hello World');

// Verify the signature
const verified = dsa.verify('Hello World', signature);

// Export the public key
const publicKey = dsa.exportKey('public');

// Export the private key
const privateKey = dsa.exportKey('private');

// Import the public key
const importedKey = DSA.importKey(publicKey);

// Import the private key
const importedKey = DSA.importKey(privateKey);

